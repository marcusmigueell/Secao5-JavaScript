/*
    Alguns eventos principais do Mouse

    Evento onclick ------> Acionado quando o elemento é clicado
    Evento ondblclick ---> Acionado quando o elemento é clicado 2 vezes
    Evento onmouseup ----> Acionado quando o clique sobre o elemento é liberado
    Evento onmouseover --> Acionado quando o cursor do mouse sobrepõe o elemento
    Evento onmouseout ---> Acionado quando o cursor do mouse sai da região da página ocupada pelo elemento

    Alguns eventos não podem ser aplicados em elementos html, para ver quais se aplicam e quais não acessar o link:
    https://www.w3schools.com/jsref/dom_obj_event.asp



    function acao() {
        alert('Evento disparado')
    }

    function acao1() {
        alert('Elemento foi clicado')
    }

    function acao2() {
        alert('O cursor do mouse deixou a região do elemento')
    }

*/

